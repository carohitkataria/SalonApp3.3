async def seed_supplier_product_samples(db) -> int:
    """Idempotent upsert of the SAMPLES list into supplier_product_samples.

    Returns the number of documents upserted (i.e. inserted or replaced).
    Safe to call on every startup — drives the canonical sample catalogue
    from code, independent of database state.
    """
    if db is None:
        return 0
    count = 0
    for sample in SAMPLES:
        await db.supplier_product_samples.update_one(
            {"id": sample["id"]},
            {"$set": sample},
            upsert=True,
        )
        count += 1
    return count
